package MyPackage;

public class Movie {
private String MovieName;
private double MovieStartTime,MovieEndTime;
private float MovieAmount;
public String getMovieName() {
	return MovieName;
}
public void setMovieName(String movieName) {
	MovieName = movieName;
}
public double getMovieStartTime() {
	return MovieStartTime;
}
public void setMovieStartTime(double movieStartTime) {
	MovieStartTime = movieStartTime;
}
public double getMovieEndTime() {
	return MovieEndTime;
}
public void setMovieEndTime(double movieEndTime) {
	MovieEndTime = movieEndTime;
}
public float getMovieAmount() {
	return MovieAmount;
}
public void setMovieAmount(float movieAmount) {
	MovieAmount = movieAmount;
}

}
